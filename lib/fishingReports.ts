/** 全レポートを返す（reviewed=trueのみ） */
export function getAllReports(): FishingReport[] {
  return FISHING_REPORTS.filter((r) => r.reviewed)
}

/** スラッグでレポートを1件取得 */
export function getReportBySlug(slug: string): FishingReport | undefined {
  return FISHING_REPORTS.find((r) => r.slug === slug && r.reviewed)
}

/** 地域スラッグでレポートを絞り込む */
export function getReportsByRegion(regionSlug: string): FishingReport[] {
  return FISHING_REPORTS.filter(
    (r) => r.regionSlug === regionSlug && r.reviewed
  )
}

/** 魚名でレポートを絞り込む */
export function getReportsByFish(fishName: string): FishingReport[] {
  return FISHING_REPORTS.filter(
    (r) => r.fishNames.includes(fishName) && r.reviewed
  )
}

/**
 * 最新レポートをn件返す（publishedAt降順）
 * reviewed=true のみ。AI生成でreviewedでないものは除外。
 */
export function getLatestReports(limit = 10): FishingReport[] {
  return [...FISHING_REPORTS]
    .filter((r) => r.reviewed)
    .sort((a, b) => b.publishedAt.localeCompare(a.publishedAt))
    .slice(0, limit)
}
